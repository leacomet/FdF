#include "fdf.h"

t_coor	fill_coor(int x_0, int x_1, int y_0, int y_1)
{
	t_coor	coor;

	coor.x_0 = x_0 + 750;
	coor.x_1 = x_1 + 750;
	coor.y_0 = y_0 + 450;
	coor.y_1 = y_1 + 450;
	return (coor);
}
