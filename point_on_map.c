#include "fdf.h"

int	point_on_map(t_coor coor)
{
	if (((coor.x_1 < 1500 && coor.x_1 >= 0)
			&& (coor.y_1 < 900 && coor.y_1 >= 0))
		|| ((coor.x_0 < 1500 && coor.x_0 >= 0)
			&& (coor.y_0 < 900 && coor.y_0 >= 0)))
		return (1);
	return (0);
}

int	coor_on_map(int x, int y)
{
	if (x <= 0 || x >= 1500 || y <= 0 || y >= 900)
		return (0);
	return (1);
}
